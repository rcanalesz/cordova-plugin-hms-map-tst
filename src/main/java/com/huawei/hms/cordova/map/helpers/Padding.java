package com.huawei.hms.cordova.map.helpers;

public class Padding {
    private int left;
    private int top;
    private int right;
    private int bottom;

    public Padding(int all) {
        left = all;
        top = all;
        right = all;
        bottom = all;
    }

    public Padding(int horizontal, int vertical) {
        left = horizontal;
        right = horizontal;

        top = vertical;
        bottom = vertical;
    }

    public Padding(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }

    public void using(Consumer<Padding> a) {
        a.run(this);
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public int getBottom() {
        return bottom;
    }

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }
}
